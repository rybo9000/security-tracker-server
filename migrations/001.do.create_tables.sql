/* CREATE BASE CLIENTS TABLE */
CREATE TABLE clients (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL
);

/* CREATE BASE SECURITY ITEMS TABLE */
CREATE TABLE securityitems (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL
);

/* CREATE BASE CATEGORIES TABLE */
CREATE TABLE categories (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL
);

/* CREATE BASE STATUS TABLE */
CREATE TABLE status (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    status BOOLEAN NOT NULL DEFAULT FALSE
);